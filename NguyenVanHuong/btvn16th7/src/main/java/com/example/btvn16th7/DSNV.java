package com.example.btvn16th7;

public class DSNV {
    private String MNV;
    private String TNV;

    public DSNV(String MNV, String TNV) {
        this.MNV = MNV;
        this.TNV = TNV;
    }

    public String getMNV() {
        return MNV;
    }

    public void setMNV(String MNV) {
        this.MNV = MNV;
    }

    public String getTNV() {
        return TNV;
    }

    public void setTNV(String TNV) {
        this.TNV = TNV;
    }
}
